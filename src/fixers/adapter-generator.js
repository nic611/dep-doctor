import { writeFileSync, mkdirSync } from 'node:fs';
import { join } from 'node:path';

// Adapter shim templates — proven patterns from dependency-hell-sim
const SHIMS = {
  findDOMNode: {
    api: 'ReactDOM.findDOMNode',
    code: `// Shim: findDOMNode → ref-based approach
export function findDOMNodeShim(component) {
  if (component instanceof HTMLElement) return component;
  console.warn('[adapter] findDOMNode is deprecated. Use refs instead.');
  // In React 18 StrictMode, findDOMNode is removed.
  // This shim returns null safely — components should migrate to useRef.
  try {
    const ReactDOM = require('react-dom');
    if (ReactDOM.findDOMNode) return ReactDOM.findDOMNode(component);
  } catch {}
  return null;
}`,
  },
  legacyContext: {
    api: 'Legacy Context (childContextTypes/getChildContext)',
    code: `// Shim: Legacy Context → React.createContext bridge
import React from 'react';

export function createLegacyContextBridge(contextTypes) {
  const contexts = {};
  for (const key of Object.keys(contextTypes)) {
    contexts[key] = React.createContext(undefined);
  }

  function LegacyProvider({ values, children }) {
    let wrapped = children;
    for (const [key, Ctx] of Object.entries(contexts)) {
      wrapped = React.createElement(Ctx.Provider, { value: values[key] }, wrapped);
    }
    return wrapped;
  }

  function useLegacyContext(key) {
    return React.useContext(contexts[key]);
  }

  return { contexts, LegacyProvider, useLegacyContext };
}`,
  },
  componentWillReceiveProps: {
    api: 'componentWillReceiveProps',
    code: `// Shim: componentWillReceiveProps → getDerivedStateFromProps
// Wrap class component to intercept prop changes safely
export function withPropChangeHandler(WrappedComponent, onPropsChange) {
  return class extends React.Component {
    static getDerivedStateFromProps(nextProps, prevState) {
      if (prevState._prevProps) {
        const changed = {};
        for (const key of Object.keys(nextProps)) {
          if (nextProps[key] !== prevState._prevProps[key]) {
            changed[key] = nextProps[key];
          }
        }
        if (Object.keys(changed).length > 0) {
          return { ...onPropsChange(nextProps, prevState, changed), _prevProps: nextProps };
        }
      }
      return { _prevProps: nextProps };
    }

    render() {
      return React.createElement(WrappedComponent, this.props);
    }
  };
}`,
  },
  stringRefs: {
    api: 'String refs (this.refs.xxx)',
    code: `// Shim: String refs → createRef bridge
// For class components that use this.refs.inputName
export function createRefBridge(refNames) {
  const refs = {};
  const callbacks = {};
  for (const name of refNames) {
    refs[name] = React.createRef();
    callbacks[name] = (el) => { refs[name].current = el; };
  }
  return { refs, callbacks };
}`,
  },
  createFactory: {
    api: 'React.createFactory',
    code: `// Shim: React.createFactory → createElement wrapper
export function createFactory(type) {
  return function(props, ...children) {
    return React.createElement(type, props, ...children);
  };
}`,
  },
  componentWillUpdate: {
    api: 'componentWillUpdate',
    code: `// Shim: componentWillUpdate → getSnapshotBeforeUpdate + componentDidUpdate
// No direct shim possible — this logs a migration guide
export function warnComponentWillUpdate(componentName) {
  console.warn(
    \`[adapter] \${componentName} uses componentWillUpdate which is removed in React 18.\\n\` +
    \`Migrate to getSnapshotBeforeUpdate + componentDidUpdate.\\n\` +
    \`See: https://reactjs.org/docs/react-component.html#getsnapshotbeforeupdate\`
  );
}`,
  },
};

export function generateAdapter(packageName, findings, outputDir, { dryRun = false } = {}) {
  // Determine which shims are needed
  const neededShims = new Set();

  for (const finding of findings) {
    if (finding.api.includes('findDOMNode')) neededShims.add('findDOMNode');
    if (finding.api.includes('Context') || finding.api.includes('context')) neededShims.add('legacyContext');
    if (finding.api.includes('componentWillReceiveProps')) neededShims.add('componentWillReceiveProps');
    if (finding.api.includes('string ref') || finding.api.includes('this.refs')) neededShims.add('stringRefs');
    if (finding.api.includes('createFactory')) neededShims.add('createFactory');
    if (finding.api.includes('componentWillUpdate')) neededShims.add('componentWillUpdate');
  }

  if (neededShims.size === 0) {
    return { success: true, message: 'No adapter shims needed', files: [] };
  }

  // Generate adapter file
  const adapterCode = [
    `// Auto-generated adapter for ${packageName}`,
    `// Generated by dep-doctor v2 — ${new Date().toISOString().split('T')[0]}`,
    `// Shims: ${[...neededShims].join(', ')}`,
    ``,
    `import React from 'react';`,
    ``,
  ];

  for (const shimName of neededShims) {
    const shim = SHIMS[shimName];
    if (shim) {
      adapterCode.push(shim.code);
      adapterCode.push('');
    }
  }

  // Generate test file
  const testCode = [
    `// Contract tests for ${packageName} adapter`,
    `import { describe, it, expect } from 'vitest'; // or jest`,
    ``,
    ...([...neededShims].map(s =>
      `describe('${s} shim', () => {\n  it('should not throw', () => {\n    // TODO: Add contract test\n    expect(true).toBe(true);\n  });\n});`
    )),
  ];

  const adapterPath = join(outputDir, `${packageName.replace(/[@/]/g, '-')}-adapter.js`);
  const testPath = join(outputDir, `${packageName.replace(/[@/]/g, '-')}-adapter.test.js`);

  if (dryRun) {
    return {
      success: true,
      dryRun: true,
      files: [adapterPath, testPath],
      shimCount: neededShims.size,
      shims: [...neededShims],
    };
  }

  try {
    mkdirSync(outputDir, { recursive: true });
    writeFileSync(adapterPath, adapterCode.join('\n'));
    writeFileSync(testPath, testCode.join('\n'));
  } catch (err) {
    return { success: false, error: `Failed to write adapter files: ${err.message}` };
  }

  return {
    success: true,
    files: [adapterPath, testPath],
    shimCount: neededShims.size,
    shims: [...neededShims],
  };
}
